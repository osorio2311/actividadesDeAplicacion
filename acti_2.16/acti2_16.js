//variable
let a = 5;

//a++
console.log("a++:");
console.log("El valor original de a es: " + a); //valor de a=5
console.log("El valor después de a++ es: " + a++);//incrementa "a" en 1 y a++ devuelve el valor original
console.log("El valor final de a es: " + a);//al final incrementa "a" en 1 = 6
console.log("----------------------------");


//a--
console.log("a--:");
console.log("El valor original de a es: " + a);//valor de a = 6
console.log("El valor después de a-- es: " + a--);//decremente "a" en 1 y a-- devuelve el valor original =6
console.log("El valor final de a es: " + a);//al final decrementa "a" en 1 = 5
console.log("----------------------------");




//++a
console.log("++a:");
console.log("El valor original de a es: " + a);//valor de a = 5
console.log("El valor después de ++a es: " + ++a);//Suma 1 y devuelve el valor a = 6
console.log("El valor final de a es: " + a);//devuelve el nuevo valor de a=6
console.log("----------------------------");




//--a
console.log("--a:");
console.log("El valor original de a es: " + a);//valor de a=6
console.log("El valor después de --a es: " + --a);//decrementa 1 y devuelve el valor a=5
console.log("El valor final de a es: " + a);//devuelve el nuevo valor de a=5
console.log("----------------------------");

